namespace Azuria.Main.Minor
{
    /// <summary>
    ///     Represents an enumeration which describes the type of an <see cref="Anime" />.
    /// </summary>
    public enum AnimeType
    {
        /// <summary>
        ///     Represents a series.
        /// </summary>
        Series,

        /// <summary>
        ///     Represents a movie.
        /// </summary>
        Movie,

        /// <summary>
        ///     Represents an "Original Video Animation".
        /// </summary>
        Ova,

        /// <summary>
        ///     Represents an unkown type.
        /// </summary>
        Unknown
    }
}