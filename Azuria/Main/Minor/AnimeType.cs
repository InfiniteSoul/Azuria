namespace Azuria.Main.Minor
{
    /// <summary>
    ///     Represents an enumeration which describes the type of an <see cref="Anime" />.
    /// </summary>
    public enum AnimeType
    {
        /// <summary>
        ///     Describes a series.
        /// </summary>
        Series,

        /// <summary>
        ///     Descibes a movie.
        /// </summary>
        Movie,

