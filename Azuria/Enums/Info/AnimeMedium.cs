namespace Azuria.Enums.Info
{
    /// <summary>
    /// Represents an enumeration which describes the type of an anime.
    /// </summary>
    public enum AnimeMedium
    {
        /// <summary>
        /// Represents a series.
        /// </summary>
        Series = 0,

        /// <summary>
        /// Represents a movie.
        /// </summary>
        Movie = 1,

        /// <summary>
        /// Represents an "Original Video Animation".
        /// </summary>
        Ova = 2,

        /// <summary>
        /// Represents a hentai.
        /// </summary>
        Hentai = 3,

        /// <summary>
        /// Represents an unkown type.
        /// </summary>
        Unknown = 8
    }
}