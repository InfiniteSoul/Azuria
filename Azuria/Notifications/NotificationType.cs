namespace Azuria.Notifications
{
    /// <summary>
    ///     Represents the type of a notification.
    /// </summary>
    public enum NotificationType
    {
        /// <summary>
        ///     Represents a friend request notification.
        /// </summary>
        FriendRequest,

        /// <summary>
        ///     Represents a news notification.
        /// </summary>
        News,

        /// <summary>
        ///     Represents a private message notification.
        /// </summary>
        PrivateMessage,

        /// <summary>
        ///     Represents an anime-notification.
        /// </summary>
        Anime,

        /// <summary>
        ///     Represents a manga-notification.
        /// </summary>
        Manga
    }
}